import UIKit

extension UIFont {
    class func mediumGotham(size: CGFloat) -> UIFont {
        return UIFont(name: "GOTHAM-MEDIUM", size: size)!
    }

    class func lightGotham(size: CGFloat) -> UIFont {
        return UIFont(name: "Gotham-Light", size: size)!
    }
}
